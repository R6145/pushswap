#include "pushswap.h"

void	s3(t_Stacks_op *s)
{
	int	*j;

	j = s->stacka->array;
	if (j[1] > j[0] && j[1] > j[2])
	{
		rra(s);
		if (j[2] > j[1])
			sa(s);
	}
	if (j[2] > j[0])
		ra(s);
	if (j[2] > j[1])
		sa(s);
}
